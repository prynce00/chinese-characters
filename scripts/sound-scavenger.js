// Base URL for the audio files
const baseUrl =
  'https://yabla.vo.llnwd.net/media.yabla.com/chinese_static/audio/alicia/'

// Function to download a file
const downloadFile = (url, dest) =>
  new Promise((resolve, reject) => {
    const file = fs.createWriteStream(dest)
    https
      .get(url, response => {
        if (response.statusCode !== 200) {
          reject(`Failed to get '${url}' (${response.statusCode})`)
          return
        }
        response.pipe(file)
        file.on('finish', () => {
          file.close(resolve)
        })
      })
      .on('error', err => {
        fs.unlink(dest, () => reject(err.message))
      })
  })

// Function to process downloading each audio file
const downloadPinyinAudio = async () => {
  for (const pinyin of pinyinWithTones) {
    const filename = `${pinyin}.mp3`
    const fileUrl = `${baseUrl}${filename}`
    const outputPath = path.join(__dirname, 'sounds', filename)

    try {
      console.log(`Downloading: ${fileUrl}`)
      await downloadFile(fileUrl, outputPath)
      console.log(`Saved: ${outputPath}`)
    } catch (error) {
      console.error(`Error downloading ${fileUrl}: ${error}`)
    }
  }
}

// Ensure the sound directory exists
if (!fs.existsSync(path.join(__dirname, 'sounds'))) {
  fs.mkdirSync(path.join(__dirname, 'sounds'))
}

